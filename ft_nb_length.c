#include "push_swap.h"

int	ft_nb_length(int nb)
{
	int	sz;

	sz = 0;
	if ((nb < 0) || (nb == 0))
	{
		sz = 1;
	}
	while (nb)
	{
		sz = sz + 1;
		nb /= 10;
	}
	return (sz);
}
